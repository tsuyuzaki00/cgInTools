class SetCurve():
    def __init__(self, name="ctrl"):
        self.name = name

    def renameTemplate(self, sel):
        getNameSplit = ngs.GetNameSplit()
        scene = getNameSplit.scene()
        num = '1'.zfill(2)
        node = getNameSplit.node(sel)
        pos = getNameSplit.pos(sel)
        rename = '_'.join([pos, self.name, node, scene, num])
        return rename

    def trsSetting(self, ctrl):
        createNull = cmds.createNode('transform')
        renameNull = self.renameTemplate(sel = createNull)
        trs = cmds.rename(createNull, renameNull)
        cmds.parent(ctrl, trs)
        return trs

    def selectPosition(self, sel, trs):
        cmds.parent(trs, sel)
        cmds.move(0, 0, 0 ,ls=True)
        cmds.rotate(0, 0, 0 , os=True)
        cmds.parent(trs ,w = True)

    #template function
    def degress_linear(self,point,name="curveName"):
        knot = list(range(len(point)))
        old_curve = cmds.curve(d=1,per=True,p=point,k=knot)
        new_curve = cmds.rename(old_curve,name)
        return new_curve

    def degress_cubic(self,point,name="curveName"):
        knot = list(range(len(point)+2))
        old_curve = cmds.curve(d=3,per=True,p=point,k=knot)
        new_curve = cmds.rename(old_curve,name)
        return new_curve

    def parent_curve(self,curves,name="curveName"):
        curve = cmds.createNode("transform",n=name)
        for curve_trs in curves:
            print(curves)
            shape = cmds.listRelatives(curve_trs)
            cmds.parent(shape,curve,r=True,s=True)
            cmds.delete(curve_trs)
            cmds.select(cl=True)
        return curve

    def line_curve(self,point,name="curveName"):
        old_curve = cmds.curve(d=1,p=point)
        new_curve = cmds.rename(old_curve,name)
        return new_curve

    def same_name_check(self,check_node):
        if cmds.objExists(check_node):
            cmds.error("It has the same name. Please change the name")
